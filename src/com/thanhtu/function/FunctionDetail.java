package com.thanhtu.function;

import java.util.List;

public class FunctionDetail {
	public String functionName;
	List<String> listTypeArgument;
	public String getFunctionName() {
		return functionName;
	}
	public void setFunctionName(String functionName) {
		this.functionName = functionName;
	}
	public List<String> getListTypeArgument() {
		return listTypeArgument;
	}
	public void setListTypeArgument(List<String> listTypeArgument) {
		this.listTypeArgument = listTypeArgument;
	}
		
}
